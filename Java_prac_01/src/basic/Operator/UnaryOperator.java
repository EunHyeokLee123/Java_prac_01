package basic.Operator;

public class UnaryOperator {
    public static void main(String[] args) {
        int a1 = 13; int a2 = 13;
        int re1 = a1--+4;
        int re2 = --a2+4;
        System.out.println(re1);
        System.out.println(re2);
        System.out.println(a1);
        System.out.println(a2);

        boolean b1 = false;
    }
}
