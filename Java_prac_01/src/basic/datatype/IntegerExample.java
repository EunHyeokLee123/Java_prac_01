package basic.datatype;

public class IntegerExample {
    public static void main(String[] args) {

        byte a1 = 127;  // byte의 최대
        short b1 = 32767;  // short의 최대
        int c1 = 2147483647;  // int의 최대
        /*
            # 리터럴: 변수의 저장되기 전의 상수값.
            자바의 정수 리터럴의 타입은 int 입니다.
            int의 범위보다 더 큰 리터럴 정수를 표현하기 위해서는 뒤에 L을 붙여
            long 타입으로 인식시켜야 합니다.
         */
        long d1 = 21474836476L;

    }
}
